'''
In Python, functions can be categorized into several basic types:

Built-in Functions: These are functions that are always available in Python, such as print(), len(), type(), and max().

User-defined Functions: These are functions defined by the user using the def keyword. They can take parameters and return values.

Lambda Functions: Also known as anonymous functions, these are small, unnamed functions defined using the lambda keyword.

Recursive Functions: Functions that call themselves to solve smaller instances of the same problem.

Higher-order Functions: Functions that take other functions as arguments or return them as results, such as map(), filter(), and reduce().

Generator Functions: Functions that use the yield keyword to return an iterator, allowing iteration over a sequence of values.
'''